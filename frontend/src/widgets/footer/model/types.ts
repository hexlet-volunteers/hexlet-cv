import { type Icon } from '@tabler/icons-react'

/**
 * Конфигурация для одной ссылки на социальную сеть.
 */
export interface SocialIcon {
  /** Уникальный идентификатор социальной сети (например, 'github'). */
  id: string
  /** Название социальной сети для доступности (используется в aria-label). */
  label: string
  /** Полная URL-ссылка на профиль. */
  link: string
  /** Компонент иконки из библиотеки `@tabler/icons-react`. */
  icon: Icon
}

/**
 * Конфигурация для обычной навигационной ссылки.
 */
export interface NavLink {
  /** Текст ссылки, отображаемый пользователю. */
  label: string
  /** Путь для внутреннего роутера или полный URL для внешней ссылки. */
  link: string
  /**
   * Флаг внешней ссылки.
   * Если `true`, ссылка откроется в новой вкладке с атрибутами безопасности.
   * @defaultValue false
   */
  isExternal?: boolean
}

/**
 * Секция навигации, объединяющая группу ссылок под одним заголовком.
 */
export interface NavSection {
  /** Заголовок группы ссылок (отображается сверху колонки). */
  title: string
  /** Массив дочерних навигационных ссылок для этой секции. */
  links: NavLink[]
}
